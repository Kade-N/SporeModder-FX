package sporemodder.files;

public class FileStructureException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -7091301181675449390L;
	public FileStructureException() { super(); }
	public FileStructureException(String message) { super(message); }
	public FileStructureException(String message, Throwable cause) { super(message, cause); }
	public FileStructureException(Throwable cause) { super(cause); }
}
